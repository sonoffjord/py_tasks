# ЗАДАЧА 6
# Дано натуральное число. Напишите программу, которая вычисляет:

# количество цифр 3 в нем;
# сколько раз в нем встречается последняя цифра;
# количество четных цифр;
# сумму его цифр, больших пяти;
# произведение цифр, больших семи (если цифр больших семи нет, то вывести 1);
# сколько раз в нем встречается цифры 0 и 5 (всего суммарно).

n = int(input())
count_3_num, count_last_num, count_even, summ, count_zero_or_five = 0, 0, 0, 0, 0
multiply = 1
temp = n % 10
while n != 0:
    if n % 10 == 3:
        count_3_num+=1 # количество цифр 3 в нем;
    if temp == n % 10:
        count_last_num+=1 # сколько раз в нем встречается последняя цифра;
    if n % 10 % 2 == 0:
        count_even+=1 # количество четных цифр;
    if n % 10 > 5:
        summ+=n % 10 # сумму его цифр, больших пяти;
    if n % 10 > 7:
        multiply*=n % 10 # произведение цифр, больших семи
    if n % 10 == 0 or n % 10 == 5:
        count_zero_or_five+=1 # сколько раз в нем встречается цифры 0 и 5
    n //= 10
print(count_3_num, count_last_num, count_even, summ, multiply, count_zero_or_five, sep='\n')