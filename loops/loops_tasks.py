# ЗАДАЧА 3
# На вход программе подается натуральное число nn.
# Напишите программу, выводящую графическое изображение
# делимости чисел от 11 до nn включительно.
# В каждой строке надо напечатать очередное число и столько символов «+»,
# сколько делителей у этого числа.

n = int(input())
for i in range(1, n+1):
    count = ''
    for j in range(1, i+1):
        if i % j == 0:
            count += '+'
    print(i, count, sep='', end='')
    print()

