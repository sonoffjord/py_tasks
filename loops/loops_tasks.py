# ЗАДАЧА 5 Сумма факториалов
# Дано натуральное число n.
# Напишите программу, которая выводит значение суммы 1!+2!+3!+...+n!.

n = int(input())
summ = 0
fact = 1
for i in range(1, n+1):
    fact *= i
    summ += fact
print(summ)

